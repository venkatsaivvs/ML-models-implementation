def euclidean_distance(point1, point2):
    """
    Calculate distance between two points using Euclidean distance formula
    
    Formula: distance = sqrt((x1-x2)² + (y1-y2)²)
    """
    # Calculate the difference in each coordinate
    diff = point1 - point2
    print(diff)
    
    # Square the differences
    squared_diff = diff ** 2
    print("squared_diff: ", squared_diff)
    # Sum the squared differences
    sum_squared = np.sum(squared_diff)
    print("sum_squared: ", sum_squared)
    
    # Take the square root
    distance = np.sqrt(sum_squared)
    
    return distance


